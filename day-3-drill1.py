#Drill String Slicing

input = "SAYEEDA_SANEEYA_IQBAL"
#firstname
print(input[8:15])
#fathersname
print(input[16:21])
#surname
print(input[0:7])
#reverse
print(input[::-1])
#alternate character
print(input[::2])
#second character in reverse order
print(input[::-2])
#first name in reverse
print(input[::-1])
#fathers name in reverse
print(input[16:21:-1])